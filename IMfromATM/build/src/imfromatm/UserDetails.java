    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        st=db.DBC();
        int i=0;
        String r=JOptionPane.showInputDialog("Enter Account No","Account No");
        if((r.equals("Account No") && r.equals(""))){
            return;
        } else{
            try {
                rs=st.executeQuery("Select * from tbluserdetails where accno='"+ r +"'");
                while(rs.next()){
                    i++;
                    cmbacctype.setSelectedItem(rs.getString("acctype"));
                    txtacc.setText(rs.getString("accno"));
                    txtname.setText(rs.getString("name"));
                    txtaddr.setText(rs.getString("address"));
                    txtmail.setText(rs.getString("email"));
                    txtmob.setText(rs.getString("regmob"));
                    txtatmpin.setText(rs.getString("atmpin"));
                    txtatmcard.setText(rs.getString("atmcard"));
                    txtbname.setText(rs.getString("bankname"));
                    txtifsc.setText(rs.getString("bcode"));
                    txtpan.setText(rs.getString("pan"));
                    accno=txtacc.getText();
                }
                if(i>0){
                    btnsave.setEnabled(false);
                    btnsearch.setEnabled(true);
                    btnedit.setEnabled(true);
                    btndelete.setEnabled(true);
                    
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Invalid Account No.", "Search", 1);
                }
            } catch (SQLException ex) {
                Logger.getLogger(BankMaster.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
}//GEN-LAST:event_btnsearchActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String a="", b="",m="",n="",c="",o="";
                    a=cmbbank.getSelectedItem().toString();
                    b=cmbifsc.getSelectedItem().toString();
                    c=txtbankpass.getText();
                    
                    if(a.equals("Select Bank")) {
                        JOptionPane.showMessageDialog(rootPane, "Please Select Bank", "Login", 0);
                        cmbbank.requestFocus(true);
                        return;
                    } else if(b.equals("Select Bank Name")){
                        JOptionPane.showMessageDialog(rootPane, "Please Select IFSC code", "Login", 0);
                        cmbifsc.requestFocus(true);
                        return;
                    } else if(c.equals("")){
                        JOptionPane.showMessageDialog(rootPane, "Please Enter Password", "Login", 0);
                        txtbankpass.requestFocus(true);
                        return;
                    }
                    
                        st=db.DBC();
                        rs=st.executeQuery("select * from login where Username='"+a+"' and password='"+c+"'");
                        if(rs.next()) {
                            m=rs.getString("username");
                            n=rs.getString("password");
                            o=rs.getString("bcode");
                        }
                        if(a.equals(m) && b.equals(o) && c.equals(n)){
                          // JOptionPane.showMessageDialog(rootPane, "Login Sucessfull","Login", 1);
                            main1 af=new main1(a,b);
                            af.setVisible(true);
                            dispose();
                            return;
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Invalid Bank Name/Bank Code/Password", "Login", 0);
                            txtuser.setText("");
                            txtpwd.setText("");
                            txtuser.requestFocus(true);
                            return;
                        }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed


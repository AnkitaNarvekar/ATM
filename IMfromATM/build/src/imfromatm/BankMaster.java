    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        st=db.DBC();
            int i=0;
            String r=JOptionPane.showInputDialog("Enter IFSC Code","IFSC Code");
            if((r.equals("IFSC Code") && r.equals(""))){
                return;
            }
            else{
            try {
                rs=st.executeQuery("Select * from tblbank where ifsccode='"+ r +"'");
             if(rs.next()){
                        i++;
                        txtname.setText(rs.getString("bankname"));
                        txtcode.setText(rs.getString("bcode"));
                        txtbranch.setText(rs.getString("branch"));
                        txtaddress.setText(rs.getString("addr"));
                        txtifsc.setText(rs.getString("ifsccode"));
                        txtcont.setText(rs.getString("contact"));
                        txtpass.setEditable(false);
                        txtrepass.setEditable(false);
                        tmpifsc=txtifsc.getText();
                        tmpbcode=txtcode.getText();
             }
                if(i>0){
                    btnsave.setEnabled(false);
                    btnsearch.setEnabled(true);
                    btnedit.setEnabled(true);
                    btndelete.setEnabled(true);
                    
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Invalid IFSC Code", "Search", 1);
                }
            } catch (SQLException ex) {
                Logger.getLogger(BankMaster.class.getName()).log(Level.SEVERE, null, ex);
            }
               
            }
    }//GEN-LAST:event_btnsearchActionPerformed


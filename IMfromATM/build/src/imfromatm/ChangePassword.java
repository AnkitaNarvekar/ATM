    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtcp.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please enter the current password", "Password", 1);
            txtcp.requestFocus();
            return;
        } else if(txtnp.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please enter the new password", "Password", 1);
            txtnp.requestFocus();
            return;
        } else if(txtrp.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please re-enter the new password", "Password", 1);
            txtrp.requestFocus();
            return;
        }
        
        try {
            if(txtnp.getText().equals(txtrp.getText())) {
                ResultSet rs1;
                st=db.DBC();
                rs1=st.executeQuery("select password from login where username='"+ lgn +"'");
                boolean res=rs1.next();
                if(res) {
                    int q=0;
                    q=st.executeUpdate("update login set password='"+txtnp.getText()+"' where password='"+txtcp.getText()+"' and username='"+ lgn +"'");
                    if(q>0) {
                        JOptionPane.showMessageDialog(rootPane, "Password changed successfully", "Password", 1);
                        txtcp.setText("");
                        txtnp.setText("");
                        txtrp.setText("");
                        return;
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Current password is invalid", "Password", 0);
                        return;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "New password does not match", "Password", 1);
                txtnp.setText("");
                txtrp.setText("");
                txtnp.requestFocus();
                return;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_jButton1ActionPerformed

